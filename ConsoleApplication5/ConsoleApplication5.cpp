#include <iostream>
#include <fstream>
#include <vector>
#include <string>
#include <algorithm>


using namespace std;

enum class SourceType {
    BOOK,
    MAGAZINE,
    NEWSPAPER
};

class Source {
private:
    SourceType type;
    string title;
    string author;
    string category;
    int year;

public:
    Source(SourceType type, const string& title, const string& author = "", const string& category = "", int year = 0)
        : type(type), title(title), author(author), category(category), year(year) {}

    SourceType getType() const { return type; }
    const string& getTitle() const { return title; }
    const string& getAuthor() const { return author; }
    const string& getCategory() const { return category; }
    int getYear() const { return year; }

    void display() const {
        cout << "Тип: " << (type == SourceType::BOOK ? "Книга" : (type == SourceType::MAGAZINE ? "Журнал" : "Газета")) << "\n"
            << "Назва: " << title << "\n";
        if (type == SourceType::BOOK) {
            cout << "Автор: " << author << "\n"
                << "Категорія: " << category << "\n";
        }
        else {
            cout << "Рік: " << year << "\n";
        }
        cout << "-------------------------\n";
    }
};

class Library {
private:
    vector<Source*> sources;

public:
    ~Library() {
        for (auto source : sources) {
            delete source;
        }
    }

    void fillDatabase() {
        int count;
        cout << "Скільки джерел додати: ";
        cin >> count;
        cin.ignore();
        for (int i = 0; i < count; ++i) {
            addSource();
        }
    }

    void addSource() {
        int typeInput;
        cout << "Оберіть тип (1 - Книга, 2 - Журнал, 3 - Газета): ";
        cin >> typeInput;
        cin.ignore();

        SourceType type = static_cast<SourceType>(typeInput - 1);
        string title, author, category;
        int year = 0;

        cout << "Введіть назву: ";
        getline(cin, title);

        if (type == SourceType::BOOK) {
            cout << "Введіть автора: ";
            getline(cin, author);
            cout << "Введіть категорію (фантастика, детектив тощо): ";
            getline(cin, category);
        }
        else {
            cout << "Введіть рік: ";
            cin >> year;
            cin.ignore();
        }

        sources.push_back(new Source(type, title, author, category, year));
        cout << "Джерело додано до бази.\n";
    }

    void displayAll() const {
        if (sources.empty()) {
            cout << "База даних порожня.\n";
            return;
        }
        for (const auto& source : sources) {
            source->display();
        }
    }

    void deleteSource(int index) {
        if (index < 0 || index >= sources.size()) {
            cout << "Невірний індекс.\n";
            return;
        }
        delete sources[index];
        sources.erase(sources.begin() + index);
        cout << "Джерело видалено.\n";
    }

    void sortSources() {
        sort(sources.begin(), sources.end(), [](Source* a, Source* b) {
            if (a->getType() == b->getType()) {
                return a->getTitle() < b->getTitle();
            }
            return a->getType() < b->getType();
            });
        cout << "Джерела відсортовані.\n";
    }

    void searchBook(const string& author, const string& title) const {
        for (const auto& source : sources) {
            if (source->getType() == SourceType::BOOK && source->getAuthor() == author && source->getTitle() == title) {
                source->display();
                return;
            }
        }
        cout << "Книгу не знайдено.\n";
    }

    void searchMagazineByYear(int year) const {
        for (const auto& source : sources) {
            if (source->getType() == SourceType::MAGAZINE && source->getYear() == year) {
                source->display();
                return;
            }
        }
        cout << "Журнал не знайдено.\n";
    }

    void filterBooksByAuthor(const string& author) const {
        for (const auto& source : sources) {
            if (source->getType() == SourceType::BOOK && source->getAuthor() == author) {
                source->display();
            }
        }
    }

    void removeNewspapersByYear(int year) {
        sources.erase(remove_if(sources.begin(), sources.end(), [&](Source* source) {
            if (source->getType() == SourceType::NEWSPAPER && source->getYear() == year) {
                delete source;
                return true;
            }
            return false;
            }), sources.end());
        cout << "Газети за " << year << " рік видалено.\n";
    }
};

int main() {
    Library library;
    int choice;

    while (true) {
        cout << "\nМеню:\n"
            << "1. Заповнити базу даних\n"
            << "2. Переглянути всі джерела\n"
            << "3. Додати джерело\n"
            << "4. Видалити джерело\n"
            << "5. Сортувати джерела\n"
            << "6. Пошук книги\n"
            << "7. Пошук журналу за роком\n"
            << "8. Фільтрація книг за автором\n"
            << "9. Видалити газети за роком\n"
            << "0. Вийти\n"
            << "Ваш вибір: ";
        cin >> choice;

        switch (choice) {
        case 1: library.fillDatabase(); break;
        case 2: library.displayAll(); break;
        case 3: library.addSource(); break;
        case 4: {
            int index;
            cout << "Введіть індекс для видалення: ";
            cin >> index;
            library.deleteSource(index);
            break;
        }
        case 5: library.sortSources(); break;
        case 6: {
            string author, title;
            cin.ignore();
            cout << "Введіть автора: ";
            getline(cin, author);
            cout << "Введіть назву: ";
            getline(cin, title);
            library.searchBook(author, title);
            break;
        }
        case 7: {
            int year;
            cout << "Введіть рік: ";
            cin >> year;
            library.searchMagazineByYear(year);
            break;
        }
        case 8: {
            string author;
            cin.ignore();
            cout << "Введіть автора: ";
            getline(cin, author);
            library.filterBooksByAuthor(author);
            break;
        }
        case 9: {
            int year;
            cout << "Введіть рік: ";
            cin >> year;
            library.removeNewspapersByYear(year);
            break;
        }
        case 0: return 0;
        default: cout << "Невірний вибір.\n";
        }
    }
}